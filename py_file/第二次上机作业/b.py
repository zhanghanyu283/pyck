#．一个物体从100m的高空自由落下，计算并输出它到达地面所需的时间和它前3s内下落的垂直距离。不计空气阻力，设重力加速度为10m/s²,计算结果保留3位小数。   
# 定义重力加速度
import math


g = 10
# 定义初始高度
h = 100
# 定义时间
t1 = 3

# 计算到达地面所需的时间
t = math.sqrt(2 * h / g)
# 计算前 3s 内下落的垂直距离
h1 = 0.5 * g * t1 ** 2

# 输出结果，保留 3 位小数
print(f"物体到达地面所需的时间为: {t:.3f} 秒")
print(f"物体前 3s 内下落的垂直距离为: {h1:.3f} 米")


